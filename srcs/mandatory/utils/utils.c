#include "so_long.h"

size_t	ft_strlen(const char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
int ft_strcmp(const char *s1, const char *s2) 
{
    while (*s1 != '\0' && *s2 != '\0') {
        if (*s1 != *s2) {
            return (*s1 - *s2);
        }
        s1++;
        s2++;
    }
    return (*s1 - *s2);
}

static int	count_lines(char *file)
{
	char	*line;
	int		fd;
	int		count;

	fd = open(file, O_RDONLY);
	if (fd == -1)
		ft_error("ERROR: Map file opening failed!\n");
	count = 0;
	while (1)
	{
		line = get_next_line(fd);
		if (!line)
			break ;
		count++;
		free(line);
	}
	return (count);
}

